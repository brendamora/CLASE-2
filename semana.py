dia = int(raw_input("ingrese el num a validar"))

if dia == 1:
    print ("domingo")
elif dia == 2:
    print ("lunes")
elif dia == 3:
    print ("martes")
elif dia == 4:
    print ("miercoles")
elif dia == 5:
    print ("jueves")
elif dia == 6:
    print ("viernes")
elif dia == 7:
    print ("de farra") 
else:
    print ("el numero no es un dia")
    
